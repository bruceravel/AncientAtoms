c  check for null or undocumented elements
      symb = sym
      call case(test,symb)
      if (symb.eq.'nu') then
          symb = 'h'
          iz   =  1
      endif
      if ( (iz.eq.84).or.(iz.eq.85).or.(iz.eq.87).or.(iz.eq.88).or.
     $     (iz.eq.89).or.(iz.eq.91).or.(iz.eq.93) ) then
          ier = 3
          if (erf) call messag('No data for Z=84,85,87-89,91,93')
