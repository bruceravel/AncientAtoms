      j = 1
15    continue
      if ((j.le.nelem).and.(symb.eq.name(j))) then
          iz  = j
      elseif (j.gt.nelem) then
          ier = 2
          if (erf) call messag('Unknown atomic symbol')
          goto 999
      else
          j  = j+1
          goto 15
      endif

c-------------------------------------------------------------
c  checking for zero energy input, if en<0 skip over abs. calc.
