      function getf0 (elemv, s)
c      double precision function getf0 (elemv, s)

c-----------------------------------------------------------------------
c  compute the normal part of the scattering amplitude for an atom at
c  a given wavelength and momentum transfer
c-----------------------------------------------------------------------
c  input:
c    elemv: symbol for element and valence, this symbol one of the ones
c           tabulated in the subroutine symbf (see below)
c    s:     value of sin(theta)/lambda at which to calculate f0
c  output:
c    getf0: value of f0 
c-----------------------------------------------------------------------
c  the formula for reconstruction of f0 is:
c            4
c   f0(s) = sum [ ai*exp(-bi*s^2) ] + c ,    s = sin(theta) / lambda
c           i=1                            ==> (lambda*s / 2pi) is the 
c                                               momentum transfer
c-----------------------------------------------------------------------
c  coef: 1..9 corresponding to a1,b1,a2,b2,a3,b3,a4,b4,c
c-----------------------------------------------------------------------
      implicit integer(i-n)
      implicit real(a-h,o-z)
c      implicit double precision(a-h,o-z)

      parameter (ncoef = 9, nvalnc = 214, minus = -1)
      dimension a(ncoef, nvalnc)
      common /sk/ a
      character*(*) elemv, elv*4
      character*4   test, el

      test = 'ab'
      el = elemv
      call case(test,el)
      if (el.eq.'nu') then
          getf0 = 0.0
          goto 99
      endif

      elv = elemv
      call symbf(elv, ivalnc)
      s2 = s**2

      sum = a(ncoef, ivalnc)
      do 10 i=1,ncoef-2,2
        sum = sum + a(i,ivalnc) * exp(minus * a(i+1,ivalnc) * s2)
 10   continue 

      getf0 = sum

 99   continue 
      return
c  end function getf0
      end

